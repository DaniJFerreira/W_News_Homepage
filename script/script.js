//for navbar hover color animation 
$(document).ready(function(){
  $('.navbar ul li a').hover(function(){
    $(this).css('color', 'hsl(5, 85%, 63%)');
    $(this).find('after').css({
      'width': '100%',
      'z-index': '-10',
      'animation':  '1s forwards',
      '-webkit-animation': 'fill 1s forwards',
      '-moz-animation':'fill 1s forwards',
      'opacity':'1'
    });

  }, function(){
  $(this).css('color', '');  //reset color to default
  $(this).find('after').css({  //reset css properties to default values 
      'width':'0%',  
      'z-index':'1',  
      'animation':'none',  
      '-webkit-animation':'none',  
      '-moz-animation':'none',  
      'opacity':'0'  
  });
//for navbar underline animation effect
  },function(){
    $('#navbar').animate({
      width: '0%',
      height: '1px'
    }, 0);
  
    $('#navbar').animate({
      width: '100%',
      height: '1px'
    }, 500);
  
    $('#navbar').animate({
      width: '100%',
      height: '100%',
      backgroundColor: '#333'
    }, 1000);  

  }, function(){
    $('h5 a','h6 a').hover({
      'color': 'hsl(5, 85%, 63%)'
    });

  });
}); 